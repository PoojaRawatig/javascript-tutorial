// 1. Arithmetic operators 

// number addition 
let num1 = 10
let num2 = 20
let num3 = num1 + num2;
console.log(num3)

// string concatenation 
let firstName = 'aman'
let lastName = 'mehta'
let fullName = firstName + ' ' + lastName;
console.log(fullName)

// subtraction 
num3 = num1- num2;
console.log(num3)

// multiplication 
num3 = num1*num2;
console.log(num3)

//division
num3 = num1/num2;
console.log(num3);

// remainder 
num3 = num2%num1;
console.log(num3)

//power 
num1 = 2;
num2 = 3;
num3 = num1**num2;  //2^3
console.log(num3)

// increment 
num3++;   // num3 = num3 + 1
console.log(num3)

// decrement 
num3--;   //num3 = num3 -1
console.log(num3)


